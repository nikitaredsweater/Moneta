"""Configuration for the ZKP NFT Service"""
from pydantic_settings import BaseSettings
from typing import Optional


class Settings(BaseSettings):
    """Application settings"""

    # Blockchain settings
    BLOCKCHAIN_RPC_URL: str = "http://localhost:8545"
    CHAIN_ID: int = 1337  # Default for local development (Ganache/Hardhat)

    # Contract settings
    NFT_CONTRACT_ADDRESS: Optional[str] = None
    NFT_CONTRACT_NAME: str = "NFT_Storage"
    NFT_CONTRACT_SYMBOL: str = "NFTS"

    # Wallet settings (for sending transactions)
    WALLET_PRIVATE_KEY: Optional[str] = None
    WALLET_ADDRESS: Optional[str] = None

    # Gas settings
    GAS_LIMIT: int = 3000000
    GAS_PRICE: Optional[int] = None  # Wei, None = auto

    # API settings
    API_HOST: str = "0.0.0.0"
    API_PORT: int = 8001
    API_TITLE: str = "ZKP NFT Service"
    API_VERSION: str = "1.0.0"

    # ZKP Service settings
    ZKP_SERVICE_URL: Optional[str] = "http://localhost:8000"

    # Event listener settings
    EVENT_POLL_INTERVAL: int = 5  # seconds
    START_BLOCK: int = 0  # Block to start listening from

    # Logging
    LOG_LEVEL: str = "INFO"

    class Config:
        env_file = ".env"
        case_sensitive = True


# Global settings instance
settings = Settings()


